package javaPackage;

public class arithop {

	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println(a+b); // addition op
		System.out.println(a-b); // Subtraction op
		System.out.println(a*b); // multiplication op
		System.out.println(a/b); // division op
	}

}
