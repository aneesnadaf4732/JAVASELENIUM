package javaPackage;

public class assignop {

	public static void main(String[] args) {
		
		int a=10;
		a += 3; // do not pass space in between +=
		System.out.println(a); // a=13
		
		a -= 4;
		System.out.println(a); // 9

		a *= 3;
		System.out.println(a);// 27
		
		a /= 3;
		System.out.println(a); //9
	}

}
