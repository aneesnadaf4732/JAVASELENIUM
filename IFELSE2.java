package javaPackage;

public class IFELSE2 {

	public static void main(String[] args) {
		int q=100;
		int w=0;
		
		if (q < w) {
			System.out.println("q is lesser then w");
		}
		else if(q > w) {
			System.out.println("q is greater then w");
		}
		else if(q == w) {
		System.out.println("q is equals to w");
		}
		else {
			System.out.println("q and w are different");
		}
	}

}
