package javaPackage;

public class FOREACHLOOP {


	public static void main(String[] args) {
		//defining an array 
		Object a[]= {"java", 10.5, 10<5, 'c', 8};
		// print an array using for each loop 
		
		// traverse the array using the for each loop 
		
		for(Object b : a) {
			System.out.println(b);
		}
	
	}

}
